    // Special MDS matrix for mixColumns() method
    private static final byte[][] MDS_MATRIX = {
            {2, 3, 1, 1},
            {1, 2, 3, 1},
            {1, 1, 2, 3},
            {3, 1, 1, 2}
    };

    // Inverted special MDS matrix for invertMixColumns() method
    private static final byte[][] INVERTED_MDS_MATRIX = {
            {14, 11, 13, 9},
            {9, 14, 11, 13},
            {13, 9, 14, 11},
            {11, 13, 9, 14}
    };


    /*
        Encrypts input text. Implemented according to lecture 4, pp. 14-22
        text is text to encrypt, its size must be 16
        128-bit key, that is necessary for algorithm, is generated by MD-5 hash-function from password
        necessary round keys are calculated from this key
     */
    public byte[] encrypt(byte[] text, String password) throws NoSuchAlgorithmException {
        byte[] key = convertPasswordToKey(password);
        byte[][] state = initState(text);
        byte[][][] roundKeys = calcRoundKeys(key);

        addRoundKey(state, roundKeys[0]);
        for(int i = 1; i <= 9; i++){
            subBytes(state);
            shiftRows(state);
            mixColumns(state);
            addRoundKey(state, roundKeys[i]);
        }

        subBytes(state);
        shiftRows(state);
        addRoundKey(state, roundKeys[10]);

        return convertStateToArray(state);
    }


    /*
        Decrypts given cipher.
        Implementation is reverse order calling inverted functions of encrypt()
        cipher is cipher to decrypt, its size must be 16
        128-bit key, that is necessary for algorithm, is generated by MD-5 hash-function from password
        necessary round keys are calculated from this key
     */
    public byte[] decrypt(byte[] cipher, String password) throws NoSuchAlgorithmException {
        byte[] key = convertPasswordToKey(password);
        byte[][] state = initState(cipher);
        byte[][][] roundKeys = calcRoundKeys(key);


        addRoundKey(state, roundKeys[10]);
        invertShiftRows(state);
        invertSubBytes(state);

        for(int i = 9; i >= 1; i--){
            addRoundKey(state, roundKeys[i]);
            invertMixColumns(state);
            invertShiftRows(state);
            invertSubBytes(state);
        }
        addRoundKey(state, roundKeys[0]);

        return convertStateToArray(state);
    }

    /*
        Сonvert string into 128-bit key using hash function MD-5
     */
    public byte[] convertPasswordToKey(String password) throws NoSuchAlgorithmException {
        MessageDigest md = MessageDigest.getInstance("MD5");
        return md.digest(password.getBytes());
    }

    /*
        Copies input array to matrix according to rule: matrix[i][j] = text[i + 4*j]
     */
    private byte[][] initState(byte[] text){
        byte[][] state = new byte[4][4];
        for(int i = 0; i < 4; i++){
            for(int j = 0; j < 4; j++){
                state[i][j] = text[i + 4*j];
            }
        }
        return state;
    }

    /*
        Copies matrix data into array according to rule: array[i + 4*j] = matrix[i][j]
     */
    private byte[] convertStateToArray(byte[][] state){
        byte[] cipher = new byte[16];
        for(int i = 0; i < 4; i++){
            for(int j = 0; j < 4; j++){
                cipher[i + 4*j] = state[i][j];
            }
        }
        return cipher;
    }

    /*
        Performs AddRoundKey operation according to lecture 4, p. 16
        Performs XOR operation between two matrices element by element
        state and roundKey are matrices 4x4
        this operation is inverted to itself
     */
    private void addRoundKey(byte[][] state, byte[][] roundKey){
        for(int i = 0; i < state.length; i++){
            for(int j = 0; j < state[i].length; j++){
                state[i][j] ^= roundKey[i][j];
            }
        }
    }

    /*
        Performs SubBytes operation according to lecture 4, pp. 16-17
        For each matrix element a:
            - calculate c = a^(-1) mod 2^8
            - a := c (in binary form) multiplied right to special binary matrix 8x8

        Instead of doing such calculations each time,
        we are using special pre-calculated mappings,
        that define result of this operation for each number in [0, 255]
     */
    private void subBytes(byte[][] state){
        for(int i = 0; i < state.length; i++){
            for(int j = 0; j < state[i].length; j++){
                state[i][j] = blockSTransformation(state[i][j]);
            }
        }
    }

    /*
        Performs inverted SubBytes operation

        Implementation is similar to .subBytes(), but uses inverted mappings
     */
    private void invertSubBytes(byte[][] state){
        for(int i = 0; i < state.length; i++){
            for(int j = 0; j < state[i].length; j++){
                state[i][j] = invertBlockSTransformation(state[i][j]);
            }
        }
    }


    // use known already calculated mapping instead of calculating transform every time
    private byte blockSTransformation(byte a){
        int valAsInt = Byte.toUnsignedInt(a);
        return Integer.valueOf(BLOCK_S[valAsInt]).byteValue();
    }

    // use known already calculated mapping instead of calculating transform every time
    private byte invertBlockSTransformation(byte a){
        int valAsInt = Byte.toUnsignedInt(a);
        return Integer.valueOf(INVERSED_BLOCK_S[valAsInt]).byteValue();
    }

    private void blockSTransformationOfArray(byte[] array){
        for(int i = 0; i < array.length; i++){
            array[i] = blockSTransformation(array[i]);
        }
    }

    /*
        Performs ShiftRows operation according to lecture 4, p. 18

        Cyclically shifts all rows: i-th row on i elements left
     */
    private void shiftRows(byte[][] state){
        // copy state
        byte[][] stateCopy = new byte[4][4];
        for(int i = 0; i < state.length; i++){
            System.arraycopy(state[i], 0, stateCopy[i], 0, state[i].length);
        }

        for(int i = 0; i < state.length; i++){
            for(int j = 0; j < state[i].length; j++){
                int jOld = (j + i) % 4;
                state[i][j] = stateCopy[i][jOld];
            }
        }
    }

    /*
        Performs inverted ShiftRows operation

        Cyclically shifts all rows: i-th row on i elements right
     */
    private void invertShiftRows(byte[][] state){
        // copy state
        byte[][] stateCopy = new byte[4][4];
        for(int i = 0; i < state.length; i++){
            System.arraycopy(state[i], 0, stateCopy[i], 0, state[i].length);
        }

        for(int i = 0; i < state.length; i++){
            for(int j = 0; j < state[i].length; j++){
                int jOld = j - i;
                if(jOld < 0){
                    jOld += 4;
                }
                state[i][j] = stateCopy[i][jOld];
            }
        }
    }


    /*
        Performs MixColumns operation according to lecture 4, pp. 19-20
        For each column col of state:
            - col considered as polynomial of 3rd power
            - col is multiplied by (3x^3 + x^2 + x + 2) modulo (x^4 + 1) in field modulo 256

        Instead of doing such calculations each time,
        we are using special pre-calculated mappings
     */
    private void mixColumns(byte[][] state){
        byte[][] stateCopy = new byte[4][4];
        for(int i = 0; i < stateCopy.length; i++){
            Arrays.fill(stateCopy[i], (byte) 0x00);
        }

        for (int i = 0; i < 4; i++) {
            for (int k = 0; k < 4; k++) {
                for (int j = 0; j < 4; j++) {
                    if (Byte.toUnsignedInt(MDS_MATRIX[i][k]) == 1){
                        stateCopy[i][j] ^= state[k][j];
                    }
                    else{
                        int row = Byte.toUnsignedInt(MDS_MATRIX[i][k]);
                        int col = Byte.toUnsignedInt(state[k][j]);
                        stateCopy[i][j] ^= FIELD_MULTIPLICATION_TABLE[row][col];
                    }
                }
            }
        }

        // state := stateCopy
        for (int i = 0; i < 4; i++) {
            System.arraycopy(stateCopy[i], 0, state[i], 0, 4);
        }
    }

    /*
        Performs operation that is inverted to MixColumns
     */
    private void invertMixColumns(byte[][] state){
        byte[][] stateCopy = new byte[4][4];
        for(int i = 0; i < stateCopy.length; i++){
            Arrays.fill(stateCopy[i], (byte) 0x00);
        }


        for (int i = 0; i < 4; i++) {
            for (int k = 0; k < 4; k++) {
                for (int j = 0; j < 4; j++) {
                    int row = Byte.toUnsignedInt(INVERTED_MDS_MATRIX[i][k]);
                    int col = Byte.toUnsignedInt(state[k][j]);
                    stateCopy[i][j] ^= FIELD_MULTIPLICATION_TABLE[row][col];
                }
            }
        }


        // state := stateCopy
        for (int i = 0; i < 4; i++) {
            System.arraycopy(stateCopy[i], 0, state[i], 0, 4);
        }
    }
    

    /*
        Performs algorithm of expanding key according to lecture 4, pp. 21-22
        key.length = 16
        returns array of 11 matrices (round keys)
     */
    private byte[][][] calcRoundKeys(byte[] key){
        byte[][][] roundKeys = new byte[11][4][4];

        // 0-th round key
        roundKeys[0] = new byte[][]{
                {key[0], key[1], key[2], key[3]},
                {key[4], key[5], key[6], key[7]},
                {key[8], key[9], key[10], key[11]},
                {key[12], key[13], key[14], key[15]}
        };

        // other round keys
        for(int i = 1; i <= 10; i++){
            byte[] t = cyclicShiftLeft(roundKeys[i - 1][3]);
            blockSTransformationOfArray(t);
            byte[] d = xor(t, getRoundConst(i));

            roundKeys[i][0] = xor(roundKeys[i-1][0], d);
            roundKeys[i][1] = xor(roundKeys[i-1][1], roundKeys[i][0]);
            roundKeys[i][2] = xor(roundKeys[i-1][2], roundKeys[i][1]);
            roundKeys[i][3] = xor(roundKeys[i-1][3], roundKeys[i][2]);
        }
        return roundKeys;
    }

    /*
        Do cyclic shift on left by 1 item
     */
    private byte[] cyclicShiftLeft(byte[] array){
        byte[] result = new byte[array.length];
        for(int i = 0; i < array.length; i++){
            result[i] = array[(i+1) % array.length];
        }
        return result;
    }

    /*
        Returns RC - round constant according to lecture 4, p. 22
        i - is number of round
        i is from 1 to 10
     */
    private byte[] getRoundConst(int i){
        int fieldNumber = 256;
        Integer x = ((int) Math.pow(2, i - 1)) % fieldNumber;

        return new byte[]{x.byteValue(), 0x00, 0x00, 0x00};
    }

    /*
        Performs XOR operation on two arrays element by element
     */
    private byte[] xor(byte[] arr1, byte[] arr2){
        int length = Math.min(arr1.length, arr2.length);
        byte[] result = new byte[length];
        for(int i = 0; i < length; i++){
            result[i] = Integer.valueOf(arr1[i] ^ arr2[i]).byteValue();
        }

        return result;
    }

